// ---- schema bootstrap (Postgres): idempotent, cheap. Full schema lives in sql/schema_pg.sql. ----
function ensure_agora_tables(PDO $db): void {
    $db->exec("CREATE TABLE IF NOT EXISTS agora_mentions (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        topic_id INT NOT NULL, reply_id INT NULL, from_user_id INT NOT NULL,
        to_username VARCHAR(64) NOT NULL, seen SMALLINT NOT NULL DEFAULT 0, created_at TIMESTAMP DEFAULT now()
    )");
    $db->exec("CREATE INDEX IF NOT EXISTS idx_mentions_to_seen ON agora_mentions(to_username, seen)");
    $db->exec("CREATE TABLE IF NOT EXISTS agora_bounties (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        topic_id INT NOT NULL UNIQUE, poster_id INT NOT NULL, reward VARCHAR(255) NOT NULL DEFAULT '',
        status TEXT NOT NULL DEFAULT 'open' CHECK (status IN ('open','claimed','done')),
        claimant_id INT NULL, created_at TIMESTAMP DEFAULT now(), updated_at TIMESTAMP DEFAULT now()
    )");
    $db->exec("CREATE INDEX IF NOT EXISTS idx_bounties_status ON agora_bounties(status)");
}

