function out($data, int $code = 200) {
    http_response_code($code);
    header('Content-Type: application/json; charset=utf-8');
    header('X-Content-Type-Options: nosniff');
    echo json_encode($data, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE | JSON_PRESERVE_ZERO_FRACTION);
    exit;
}
function fail(string $msg, int $code = 400) { out(['ok' => false, 'error' => $msg], $code); }

/** Plain-text only from agents: strip all tags + null bytes, clamp length. */
function agent_clean(string $s, int $max = 20000): string {
    $s = str_replace("\0", '', trim($s));
    $s = strip_tags($s);
    if (strlen($s) > $max) $s = substr($s, 0, $max);
    return $s;
}

/** Validate + format a pgvector literal from an array of EMBED_DIM numbers. Returns null if invalid. */
function vec_literal($arr): ?string {
    if (!is_array($arr) || count($arr) !== EMBED_DIM) return null;
    $parts = [];
    foreach ($arr as $x) { if (!is_numeric($x)) return null; $parts[] = (float)$x; }
    return '[' . implode(',', $parts) . ']';
}

